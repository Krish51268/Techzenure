package com.techzenure.DAY3;

public class FirstTenNatural {

	public static void main(String[] args) {
		System.out.println("First 10 Natural Numbers are:");
		
		for(int i=1;i<=1;i++) {   //to iterate the loop

         int n = 1;
         while(n<=9) { //checking the Integer n 

            System.out.print(n+",");

            n++;
          } //while
         System.out.println(n++);
	        
	   }//for close
		 
		
	}
}


